#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import {
  mkdirSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  rmSync,
  statSync,
  unlinkSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import $RefParser from "@apidevtools/json-schema-ref-parser";

const __dirname = dirname(fileURLToPath(import.meta.url));
const TS_ROOT = resolve(__dirname, "..");
const PY_ROOT = resolve(TS_ROOT, "..", "autocontext");

const SRC_DIR = join(TS_ROOT, "src/integrations/browser/contract/json-schemas");
const DST_SCHEMAS_DIR = join(PY_ROOT, "src/autocontext/integrations/browser/contract/json_schemas");
const MODELS_PY_PATH = join(PY_ROOT, "src/autocontext/integrations/browser/contract/models.py");

const URL_PREFIX = "https://autocontext.dev/schema/browser/";
const AGGREGATE_FOR_PYDANTIC = "browser-contract.schema.json";
const checkOnly = process.argv.slice(2).includes("--check");

let drift = false;
const actions = [];

if (!checkOnly) {
  mkdirSync(DST_SCHEMAS_DIR, { recursive: true });
}

const srcSchemas = readdirSync(SRC_DIR).filter((f) => f.endsWith(".schema.json")).sort();

for (const file of srcSchemas) {
  const src = join(SRC_DIR, file);
  const dst = join(DST_SCHEMAS_DIR, file);
  const srcBytes = readFileSync(src);
  let dstBytes = null;
  try {
    dstBytes = readFileSync(dst);
  } catch {
    // first run
  }
  if (dstBytes === null || !srcBytes.equals(dstBytes)) {
    if (checkOnly) {
      drift = true;
      actions.push(`drift: schema ${file}`);
    } else {
      writeFileSync(dst, srcBytes);
      actions.push(`wrote schema: ${file}`);
    }
  }
}

let dstSchemaListing = [];
try {
  dstSchemaListing = readdirSync(DST_SCHEMAS_DIR).filter((f) => f.endsWith(".schema.json"));
} catch {
  // first run
}
const srcSet = new Set(srcSchemas);
for (const file of dstSchemaListing) {
  if (!srcSet.has(file)) {
    const target = join(DST_SCHEMAS_DIR, file);
    if (checkOnly) {
      drift = true;
      actions.push(`stale schema: ${file} should be deleted`);
    } else if (statSync(target).isFile()) {
      unlinkSync(target);
      actions.push(`deleted schema: ${file}`);
    }
  }
}

const localResolver = {
  order: 1,
  canRead: (file) => file.url.startsWith(URL_PREFIX),
  read: (file) => {
    const filename = file.url.slice(URL_PREFIX.length).replace(".json", ".schema.json");
    return readFileSync(join(SRC_DIR, filename), "utf-8");
  },
};

const aggregateEntry = JSON.parse(readFileSync(join(SRC_DIR, AGGREGATE_FOR_PYDANTIC), "utf-8"));
const bundled = await $RefParser.bundle(aggregateEntry, {
  resolve: { local: localResolver, http: false },
});

const tmpDir = mkdtempSync(join(tmpdir(), "autoctx-browser-pydantic-gen-"));
const bundledPath = join(tmpDir, "browser-contract.bundled.json");
const generatedPath = join(tmpDir, "models.py");
writeFileSync(bundledPath, JSON.stringify(bundled, null, 2));

let generatedBody;
try {
  execFileSync(
    "datamodel-codegen",
    [
      "--input", bundledPath,
      "--input-file-type", "jsonschema",
      "--output", generatedPath,
      "--output-model-type", "pydantic_v2.BaseModel",
      "--use-annotated",
      "--use-title-as-name",
      "--use-type-alias",
      "--enum-field-as-literal", "all",
      "--collapse-root-models",
      "--field-constraints",
      "--disable-timestamp",
      "--target-python-version", "3.11",
    ],
    { stdio: ["ignore", "pipe", "pipe"] },
  );
  generatedBody = readFileSync(generatedPath, "utf-8");
} finally {
  rmSync(tmpDir, { recursive: true, force: true });
}

const banner = `# AUTO-GENERATED from ts/src/integrations/browser/contract/json-schemas/ — DO NOT EDIT.
# Run: node ts/scripts/sync-python-browser-contract-schemas.mjs
# CI gate: node ts/scripts/sync-python-browser-contract-schemas.mjs --check
`;

const withBanner = generatedBody.replace(
  /^# generated by datamodel-codegen:\n#\s+filename:\s+\S+\n/,
  banner,
);

if (withBanner === generatedBody) {
  console.error("sync:browser-schemas: datamodel-codegen output banner did not match expected shape.");
  process.exit(2);
}

let existingBody = null;
try {
  existingBody = readFileSync(MODELS_PY_PATH, "utf-8");
} catch {
  // first run
}

if (existingBody !== withBanner) {
  if (checkOnly) {
    drift = true;
    actions.push("drift: browser models.py regeneration produces different output");
  } else {
    writeFileSync(MODELS_PY_PATH, withBanner);
    actions.push("wrote models.py");
  }
}

if (checkOnly) {
  if (drift) {
    console.error("Python browser contract sync has drift:");
    for (const action of actions) {
      console.error(`  ${action}`);
    }
    console.error("Run: node scripts/sync-python-browser-contract-schemas.mjs");
    process.exit(1);
  }
  console.log("Python browser schemas + models are up to date.");
  process.exit(0);
}

for (const action of actions) {
  console.log(action);
}
if (actions.length === 0) {
  console.log("Python browser schema sync unchanged.");
}
